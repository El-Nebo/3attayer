BallPlayer1Collision Proc Near
	;first condition: BALL_X < PLAYER_X + PLAYER_WIDTH
	                        mov         bx, Player1_X
	                        add         bx, Player1Width
	                        cmp         bx, Ball_X
	                        JLE         FALSE
	
	;second condition: BALL_X + BALL_SIZE > PLAYER_X
	                        mov         bx, Ballsize
	                        add         bx, Ball_X
	                        cmp         bx, Player1_X
	                        JLE         FALSE
	
	;third condition: BALL_Y < PLAYER_Y + PLAYER_HIGHT
	                        mov         bx, Player1_Y
	                        add         bx, Player1Height
	                        cmp         bx, Ball_Y
	                        JLE         FALSE
	
	;fourth condition: BALL_Y + BALL_SIZE > PLAYER_Y
	                        mov         bx, Ball_Y
	                        add         bx, Ballsize
	                        cmp         bx, Player1_Y
	                        JLE         FALSE
	
	;TRUE
	                        mov         AX, 1

	FALSE:                  
	                        RET
BallPlayer1Collision endp

