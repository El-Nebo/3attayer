MoveBall PROC	NEAR


	                        cmp         Ball_Y,0-Ballsize
	                        JG          CLEARBALLHERE
	                        mov         dx,0
	                        clearArea   Ballsize, Ballsize, dx, Ball_X
	                        JMP         CALCULATEMOVEHERE
