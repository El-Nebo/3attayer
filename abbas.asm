	;--------Calculating New Y
	CALCULATEMOVEHERE:      
	                        mov         ah,0
	                        mov         al,g
	                        mov         bl,deltaT
	                        imul        bl
	                        add         BallVerticalVelocity,ax
	                        mov         ax,BallVerticalVelocity
	                        add         ax,g/2
	;;;;;; dx mush carry sign flag of ax
	                        CWD
	;;;;;;;;;;;;
	                        mov         bx,DivisionConstant
	                        IDIV        bx
	                        add         Ball_Y,ax
	;--------------Calculating New X
	                        mov         ax,BallHorizontalVelocity
